// Declarar as variáveis
let entradaA;
let entradaB;
let A;
let B;
let PROD;

// Entrada de dados
entradaA = prompt('Digite A: ');
entradaB = prompt('Digite B: ');

// Processamento dos dados
A = parseInt(entradaA);
B = parseInt(entradaB);
PROD = A * B;

// Saída de dados
console.log(`${'PROD = ' + PROD}`);
